const express = require("express");
const app = express();
const port = 3000;

app.get("/", (req, res) => {
  res.send("Hello World");
  console.log("Server sent Hello World");
});

async function startServer(req, res) {
  app.listen(port, () => {
    console.log(`Server is listening to port ${port}`);
  });
}
startServer();
